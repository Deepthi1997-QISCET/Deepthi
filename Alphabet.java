import java.util.*;
import java.lang.*;
import java.io.*;
class Alphabet
{
	public static void main (String[] args) throws java.lang.Exception
	{
	            char c='A';
		if((c>='a'&&c<='z')||(c>='A'&&c<='Z'))
		{
			System.out.println(c + "is a alphabet");
		}
		else
		{
			System.out.println(c + "is not an alphabet");
		}
	}
}
